package first;

public class VariableInitializationExample {

	public static void main(String[] args) {
		int x;  //정수형 변수 선언
		x = 10; //변수에 정수 10을 할당(대입,저장)
		System.out.println("x = " + x);
		
		x = 20; //정수형 x변수에 20을 재할당
		System.out.println("x = " + x);
		
		int y = 20; //정수형 변수 선언 + 초기화
		System.out.println("y = " + y);
	}
}
